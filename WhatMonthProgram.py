#This program evaluates the name of the month using the number form.
#Example: if the month is number 3, then the name is march
month= int(input("Please give me your month in number form so that I may determine its name!"))
if month==1:
  print("January")
elif month == 2:
  print("February")
elif month == 3:
  print("March")
elif month == 4:
  print("April")
elif month == 5:
  print("May")
elif month == 6:
  print("June")
elif month == 7:
  print("July")
elif month == 8:
  print("August")
elif month == 9:
  print("September")
elif month == 10:
  print("October")
elif month == 11:
  print("November")
else:
  print("December")
